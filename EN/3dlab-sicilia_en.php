              3DLab-Sicily aims to create, develop, validate, and promote a sustainable regional network of three (3) 
              VR/AR and 3D visualization centers: one located in the province of Catania (at SWING:IT), 
              one in Troina (EN, at IRCCS-OASI), and one in Palermo (at IEMEST). 
              This network—believed to be the first of its kind in Sicily—is referred to as the “3DLab-Sicily infrastructure.” 
              The project also validates and demonstrates the equipment and services of the infrastructure 
              through a series of “use cases” (some identified during proposal preparation and others selected competitively during the project) 
              aligned with the Smart Specialisation Strategy (S3) of the Sicilian Region. 
              These applications and products may be brought to market and generate profit after the project’s conclusion. 
              Additional goals include linking the infrastructure with VISIONAIR to develop international markets 
              and defining post-project management, business, and sustainability models. 
              A major element of the 3DLab-Sicily work plan—fully embracing Open Innovation and Open Innovation 2.0 concepts, 
              with triple and “quadruple helix” engagement—is to establish within the infrastructure a service inspired by the concept of the “liquid lab.” 
              “Liquid labs” (known in industry as “liquid studios”) are real and virtual environments leveraging innovative ICT solutions 
              to help companies accelerate their innovation path by turning ideas into concrete applications and services 
              through the use of innovative technologies, intelligent automation, and new software development methods. 
              The 3DLab-Sicily liquid lab is designed and implemented to (i) enable the rapid development of VR/AR and 3D visualization applications 
              and (ii) provide full support to all innovation stakeholders within the “quadruple helix.” 
